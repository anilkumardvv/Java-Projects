package operators;

public class IncrementDecrement 
{

	public static void main(String[] args) 
	{
	
		//INCREMENT
		int a = 15;
		
		//a = a + 1;
		
		
		System.out.println("The initial value of a is");
		System.out.println(a);
		a++;
		System.out.println("The incremented value of a is ");
		System.out.println(a);
		System.out.println("The final value of a is");
		System.out.println(a);
		
		//DECREMENT
		int b = 10;
		//b = b - 1;
		
		System.out.println();
		System.out.println("The initial value of b is");
		System.out.println(b);
		b--;
		System.out.println("The decremented value of b is ");
		System.out.println(b);
		System.out.println("The final value of b is ");
		System.out.println(b);
		
	}

}
